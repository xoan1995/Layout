<h1>Ha Noi</h1>
<p>Hà Nội là thủ đô của nước Cộng hòa Xã hội chủ nghĩa Việt Nam, cũng là kinh đô của hầu hết các vương triều phong kiến
    tại Việt Nam trước đây. Do đó, lịch sử Hà Nội gắn liền với sự thăng trầm của lịch sử Việt Nam qua các thời kỳ.[6] Hà
    Nội là thành phố trực thuộc trung ương có diện tích lớn nhất cả nước từ khi tỉnh Hà Tây sáp nhập vào, đồng thời cũng
    là địa phương đứng thứ nhì về dân số với hơn 8 triệu người (năm 2019), tuy nhiên, nếu tính những người cư trú không
    đăng ký thì dân số thực tế của thành phố này năm 2019 là gần 10 triệu người. Mật độ dân số của Hà Nội là 2.398
    người/km², mật độ giao thông là 105,2 xe/km² mặt đường. Hiện nay, Hà Nội và Thành phố Hồ Chí Minh là hai đô thị loại
    đặc biệt của Việt Nam.</p>
<p>Hà Nội được Tổ chức Khoa học, Văn hóa và Giáo dục của Liên hợp quốc (UNESCO) trao tặng danh hiệu “Thành phố vì hòa
    bình” ngày 16/7/1999.

    Hà Nội thường xuyên nằm ở top đầu các thành phố ô nhiễm, thậm chí nhiều ngày trong năm là thành phố ô nhiễm không
    khí nhất thế giới, với chỉ số bụi mịn ở mức nguy hiểm cho sức khỏe con người[8][9][10][11].</p>
